                conexaoSQLite.conectar();

                String sqlInsert = "INSERT INTO usuarios("
                + "cod_user,"
                + "nome,"
                + "login,"
                + "senha,"
                + "admin) VALUES(null,?,?,?,?)"
                + ";";

