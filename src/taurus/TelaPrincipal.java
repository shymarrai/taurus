    private void btnProcurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcurarActionPerformed
        // TODO add your handling code here:
        LimparTabelaUser();
        ResultSet resultSet = null;
        Statement statement = null;
        conexaoSQLite.conectar();

        statement = conexaoSQLite.criarStatement();

        try{

            String query = "SELECT * FROM usuarios;";
            resultSet = statement.executeQuery(query);
            int i = 0;

            while(resultSet.next()){
                tblResult.setValueAt(Integer.toString(resultSet.getInt("cod_user")),i,0);
                tblResult.setValueAt((resultSet.getString("nome")),i,1);
                tblResult.setValueAt((resultSet.getString("login")),i,2);
                tblResult.setValueAt((resultSet.getString("senha")),i,3);
                tblResult.setValueAt(Integer.toString(resultSet.getInt("admin")),i,4);
                i++;
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,"Query de pesquisa nao executada.");
        }
        conexaoSQLite.desconectar();
    }//GEN-LAST:event_btnProcurarActionPerformed

