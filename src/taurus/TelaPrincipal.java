    private void btnAltSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAltSalvarActionPerformed
        // TODO add your handling code here:
        
        
       
            String id = txtIdAltProd.getValue().toString();
            String nome = txtAltNome.getText();
            String tel = txtAltTel.getText();
            String cel = txtAltCel.getText();
            String end = txtAltEnd.getText();
            String obs = txtAltObs.getText();
            
        
        conexaoSQLite.conectar();

        
        
        

             String query = "UPDATE clientes SET nome = ? , telefone = ?, celular = ?, end = ?,observacao = ?  WHERE cod_cliente = ? ;";
        
              PreparedStatement preparedStatement =  conexaoSQLite.criarPreparedStatement(query);
        try{
           
            preparedStatement.setString(1,nome);
            preparedStatement.setString(2,tel);
            preparedStatement.setString(3,cel);
            preparedStatement.setString(4,end);
            preparedStatement.setString(5,obs);
            preparedStatement.setString(4,id);

            int resultado = preparedStatement.executeUpdate();
            
            if(resultado == 1){
           JOptionPane.showMessageDialog(null,"Alterado com sucesso!");
            }else{
                JOptionPane.showMessageDialog(null,"Falha na alteração!");
            }
            
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
        conexaoSQLite.desconectar();
        
        txtAltPesquisa.setText("");
        txtAltNome.setText("");
        txtAltTel.setText("");
        txtAltCel.setText("");
        txtAltEnd.setText("");
        txtAltObs.setText("");
        
    }//GEN-LAST:event_btnAltSalvarActionPerformed

