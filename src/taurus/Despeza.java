    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nome = txtNomeDes.getText();
        String valor = txtValorDes.getText();
        String obs = txtObsDes.getText();
        String data = txtDataDes.getText();
        System.out.println(data);
        
        conexaoSQLite.conectar();

                
                String sqlInsert = "INSERT INTO despeza("
                + "cod_despeza,"
                + "nome,"
                + "valor,"
                + "obs,"
                + "data_despeza"        
                + ") VALUES(null,?,?,?,?);";


                PreparedStatement preparedStatement =  conexaoSQLite.criarPreparedStatement(sqlInsert);

                try{
                    
                   
                    preparedStatement.setString(1,nome);
                    preparedStatement.setString(2,valor);
                    preparedStatement.setString(3,obs);
                    preparedStatement.setString(4,data);

                   

                    int resultado = preparedStatement.executeUpdate();

                    if(resultado == 1){
                        JOptionPane.showMessageDialog(null,"Cadastrado com sucesso!");
                    }else{
                        JOptionPane.showMessageDialog(null,"Falha no cadastro.Tente novamente.");
                    }

                }catch(SQLException e){
                    JOptionPane.showMessageDialog(null,e);
                    
                }finally{
                    if(preparedStatement != null){
                        try {
                            preparedStatement.close();
                            conexaoSQLite.desconectar();
                            txtNomeDes.setText("");
                            txtValorDes.setText("");
                            txtObsDes.setText("");
                            txtDataDes.setText("");
                        } catch (SQLException ex) {
                            Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                    conexaoSQLite.desconectar();
                }
        
    }//GEN-LAST:event_jButton1ActionPerformed
private void LimparTabela() {
        int linhas = 0;
        int colunas = 0;
        String zer = null;

        for (linhas = 0; linhas <= tblDesp.getRowCount() - 1; linhas++) {
            for (colunas = 0; colunas <= tblDesp.getColumnCount() - 1; colunas++) {
                tblDesp.setValueAt(zer, linhas, colunas);
            }
        }
       
    }
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        LimparTabela();
        ResultSet resultSet = null;
        Statement statement = null;
        conexaoSQLite.conectar();

        statement = conexaoSQLite.criarStatement();
  
        
        try{
           
            String query = "SELECT * FROM despeza;";
            
            resultSet = statement.executeQuery(query);
            int i = 0;
                
            while(resultSet.next()){
               
                tblDesp.setValueAt((resultSet.getString("cod_despeza")),i,0);
                tblDesp.setValueAt((resultSet.getString("nome")),i,1);
                tblDesp.setValueAt((resultSet.getString("valor")),i,2);
                tblDesp.setValueAt((resultSet.getString("obs")),i,3);
                tblDesp.setValueAt((resultSet.getString("data_despeza")),i,4);
                i++;
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,"Query de pesquisa nao executada.");
        }
        conexaoSQLite.desconectar();
        
    }//GEN-LAST:event_jButton3ActionPerformed

